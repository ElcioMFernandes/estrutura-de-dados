## Construa um programa que, a partir da lista L desordenada,
## tenha uma função que cria uma nova lista
## K ordenada, com os
## mesmos dados da lista L.
## A função deve remover os elementos da lista L, e inserir na lista K,
## que dessa forma torna
## se uma lista ordenada (em ordem crescente).
## Observações
## No final do processo, a lista L estará vazia e a lista K conterá os
## dados anteriormente alocados para a lista L.
