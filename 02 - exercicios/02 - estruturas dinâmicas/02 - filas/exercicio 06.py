## Faça um programa de gerenciamento de ordem para atendimento ao cliente.
## O cliente chegará para ser atendido, receberá uma senha sequencial e aguardará na
## fila até ser chamado. A senha estará atrelada ao cartão fidelidade do cliente que
## possui nome e motivo do atendimento. (Gere a senha automaticamente e armazene
## todos os atributos no mesmo objeto).
## Ao ser chamado para atendimento, o primeiro cliente da fila é removido.
## É possível que os atendentes confiram todos os dados referente aos clientes que
## estão aguardando na fila (seguindo o conceito de fila, apenas o primeiro pode ser
## manipulado).
## Ao final do dia de trabalho, é necessário gerar um relatório onde constem todos os
## atendimento realizados naquele período, com o horário de atendimento de cada
## cliente (Adicione um atributo ao cliente e manipule uma fila a parte para gerar esse
## relatório).
## =====As opções de menu são: ======
## 1 - Receber cliente na fila.
## 2 - Chamar para atendimento.
## 3 - Visualizar fila de clientes aguardando.
## 4 - Visualizar fila de clientes atendidos.
## Obs: Lembre de indicar o horário de atendimento de cada cliente.